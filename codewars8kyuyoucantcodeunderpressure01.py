'''
You Can't Code Under Pressure #1 8kyu

Code as fast as you can! You need to double the integer and return it.
'''
def double_integer(i):
    return i * 2


print(double_integer(2)) #print 4
print(double_integer(4)) #print 8
print(double_integer(-10)) #print -20
print(double_integer(0)) #print 0
print(double_integer(100)) #print 200